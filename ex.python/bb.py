import math #importa a biblioteca math para usar funções matemáticas

#math pow é uma função que calcula a potência de um número.
print("2 elevado a 5 é " + str(math.pow(2, 5)))

print(math.ceil(2.3)) #math ceil é uma função que arredonda um número para cima.

print(math.floor(2.3)) #math floor é uma função que arredonda um número para baixo.

print(math.fabs(15 - 23)) #math fabs é uma função que retorna o valor absoluto de um número.

print(math.log(1000, 10)) #math log é uma função que retorna o logaritmo de um número em uma base específica. Neste caso, o logaritmo de 1000 na base 10 é 3, porque 10 elevado a 3 é igual a 1000.


